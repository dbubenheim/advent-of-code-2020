        @JvmStatic
        fun tobbogganTrajectory(right: Int = 3, down: Int = 1): Long {

            var count = 0L
            var y = 0
            var x = 0
            do {

                x += right
                if (x > MAX_X) {
                    x %= MAX_X
                    x--
                }
                y += down
                val field = INPUT[y][x]
                if (field == TREE) count++

            } while (y < MAX_Y)

            return count
        }

